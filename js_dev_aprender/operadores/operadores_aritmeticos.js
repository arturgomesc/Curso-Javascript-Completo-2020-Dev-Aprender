//operadores aritméticos
let salario = 100;

// + , - , * , / , **
console.log(salario + salario);
console.log(salario - salario);
console.log(salario * salario);
console.log(salario / salario);
console.log(5 ** 5);

// ++ , -- (incrementação e decrementação)
let idade1 = 18;
let idade2 = 22;

console.log(idade1++); // só é possível ver o resultado na próxima linha
console.log(idade1);
console.log(++idade1); // para ver na mesma linha

console.log(idade2--);
console.log(idade2);
console.log(--idade2); //para ver na mesma linha